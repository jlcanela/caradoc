module UniqueId = struct

  type t = (string, int) Hashtbl.t

  let create () : t =
    Hashtbl.create 16

  let id (x : t) (s : string) : int =
    try
      Hashtbl.find x s
    with Not_found ->
      let len = Hashtbl.length x in
      Hashtbl.add x s len;
      len

end

